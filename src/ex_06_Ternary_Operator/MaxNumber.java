package ex_06_Ternary_Operator;

public class MaxNumber {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        // Checking which number is larger using ternary operator
        System.out.println(num1 > num2 ? "Num1 is larger than Num2: "+num1 : "Num2 is larger than Num1: "+num2);
    }
}
